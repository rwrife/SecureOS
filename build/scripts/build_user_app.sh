#!/usr/bin/env bash
# build_user_app.sh - Compile a user-space application into SOF format
#
# This script runs INSIDE the Docker toolchain container. It compiles a
# user app from user/apps/<name>/ into artifacts/user/<name>.bin (SOF).
# Called by: build/scripts/build.sh
#
# Usage: build_user_app.sh [app_name]  (default: filedemo)
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
OUT_DIR="$ROOT_DIR/artifacts/user"
APP_NAME="${1:-filedemo}"

build_user_app_inner() {
  APP_DIR="user/apps/$APP_NAME"
  APP_OUT_DIR="artifacts/user/$(dirname "$APP_NAME")"
  test -f "$APP_DIR/main.c"
  EXTRA_OBJECTS=""
  NETLIB_OBJECTS=""
  USER_CFLAGS="--target=x86_64-unknown-none-elf -ffreestanding -fno-stack-protector -mno-red-zone -I user/include"
  USER_LDFLAGS="-m elf_x86_64 -nostdlib -e main --image-base=0x800000"

  if [ -d "$APP_DIR/resources" ]; then
    python3 - "$APP_DIR/resources" "artifacts/user/${APP_NAME}_resources_gen.c" <<'PY'
import pathlib
import sys

res_dir = pathlib.Path(sys.argv[1])
out_path = pathlib.Path(sys.argv[2])
entries = []

for txt_file in sorted(res_dir.glob('*.txt')):
    key = txt_file.stem
    text = txt_file.read_text(encoding='utf-8')
    esc = text.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n"\n    "')
    entries.append((key, esc))

out_path.parent.mkdir(parents=True, exist_ok=True)
with out_path.open('w', encoding='utf-8', newline='\n') as f:
    f.write('/* Auto-generated by build_user_app.sh. Do not edit manually. */\n')
    f.write('typedef struct {\n')
    f.write('  const char *name;\n')
    f.write('  const char *text;\n')
    f.write('} help_resource_entry_t;\n\n')
    f.write('const help_resource_entry_t g_help_resources[] = {\n')
    for key, esc in entries:
        f.write(f'  {{"{key}",\n')
        f.write(f'    "{esc}"}},\n')
    f.write('};\n\n')
    f.write('const unsigned int g_help_resource_count = ')
    f.write(str(len(entries)))
    f.write('u;\n')
PY

    clang $USER_CFLAGS -c "artifacts/user/${APP_NAME}_resources_gen.c" -o "artifacts/user/${APP_NAME}_resources_gen.o"
    EXTRA_OBJECTS="artifacts/user/${APP_NAME}_resources_gen.o"
  fi

  mkdir -p artifacts/user
  mkdir -p "$APP_OUT_DIR"

  # Compile all .c files in the app directory (multi-file app support)
  APP_OBJECTS=""
  for src_file in "$APP_DIR"/*.c; do
    if [ -f "$src_file" ]; then
      src_base="$(basename "$src_file" .c)"
      obj_path="artifacts/user/${APP_NAME}_${src_base}.o"
      clang $USER_CFLAGS -I "$APP_DIR" -c "$src_file" -o "$obj_path"
      APP_OBJECTS="$APP_OBJECTS $obj_path"
    fi
  done

  # Check for netlib dependency (scan all .c files in the app)
  NETLIB_OBJECTS=""
  if grep -rEq '^[[:space:]]*#include[[:space:]]+"lib/netlib.h"' "$APP_DIR/"*.c 2>/dev/null; then
    NETLIB_CFLAGS="$USER_CFLAGS"
    HAVE_BEARSSL=0
    if compgen -G "artifacts/bearssl/*.o" >/dev/null; then
      HAVE_BEARSSL=1
      NETLIB_CFLAGS="$NETLIB_CFLAGS -nostdlibinc -I vendor/bearssl/include -I vendor/bearssl/BearSSL/inc"
    fi

    clang $NETLIB_CFLAGS -c user/libs/netlib/api.c -o artifacts/user/netlib_api.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/backend_user.c -o artifacts/user/netlib_backend_user.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/eth.c -o artifacts/user/netlib_eth.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/arp.c -o artifacts/user/netlib_arp.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/ipv4.c -o artifacts/user/netlib_ipv4.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/udp.c -o artifacts/user/netlib_udp.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/dns.c -o artifacts/user/netlib_dns.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/tcp.c -o artifacts/user/netlib_tcp.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/http.c -o artifacts/user/netlib_http.o
    clang $NETLIB_CFLAGS -c user/libs/netlib/url_scheme.c -o artifacts/user/netlib_url_scheme.o

    NETLIB_OBJECTS="artifacts/user/netlib_api.o artifacts/user/netlib_backend_user.o artifacts/user/netlib_eth.o artifacts/user/netlib_arp.o artifacts/user/netlib_ipv4.o artifacts/user/netlib_udp.o artifacts/user/netlib_dns.o artifacts/user/netlib_tcp.o artifacts/user/netlib_http.o artifacts/user/netlib_url_scheme.o"

    if [ "$HAVE_BEARSSL" -eq 1 ]; then
      clang $NETLIB_CFLAGS -c user/libs/netlib/tls.c -o artifacts/user/netlib_tls.o
      clang $NETLIB_CFLAGS -c user/libs/netlib/https.c -o artifacts/user/netlib_https.o
      clang $NETLIB_CFLAGS -c user/libs/netlib/entropy.c -o artifacts/user/netlib_entropy.o
      clang $NETLIB_CFLAGS -c user/libs/netlib/ca_bundle.c -o artifacts/user/netlib_ca_bundle.o
      NETLIB_OBJECTS="$NETLIB_OBJECTS artifacts/user/netlib_tls.o artifacts/user/netlib_https.o artifacts/user/netlib_entropy.o artifacts/user/netlib_ca_bundle.o"
    else
      cat > artifacts/user/netlib_https_stub.c <<'EOF'
#include "https.h"

https_result_t https_request(const http_request_t *req, http_response_t *resp) {
  (void)req;
  (void)resp;
  return HTTPS_ERR_TLS;
}
EOF
      clang --target=x86_64-unknown-none-elf -ffreestanding -fno-stack-protector -mno-red-zone \
        -I user/include -I user/libs/netlib \
        -c artifacts/user/netlib_https_stub.c -o artifacts/user/netlib_https_stub.o
      NETLIB_OBJECTS="$NETLIB_OBJECTS artifacts/user/netlib_https_stub.o"
      echo "WARNING: Building netlib without TLS/HTTPS BearSSL support"
    fi

    # Link BearSSL objects if available
    BEARSSL_OBJECTS=""
    if [ "$HAVE_BEARSSL" -eq 1 ] && [ -d "artifacts/bearssl" ]; then
      for bobj in artifacts/bearssl/*.o; do
        [ -f "$bobj" ] && BEARSSL_OBJECTS="$BEARSSL_OBJECTS $bobj"
      done
    fi
    NETLIB_OBJECTS="$NETLIB_OBJECTS $BEARSSL_OBJECTS"
  fi

  clang $USER_CFLAGS -c user/runtime/secureos_api_stubs.c -o artifacts/user/secureos_api_stubs.o
  ld.lld $USER_LDFLAGS \
      -o "artifacts/user/$APP_NAME.elf" $APP_OBJECTS artifacts/user/secureos_api_stubs.o $EXTRA_OBJECTS $NETLIB_OBJECTS

  # Build sof_wrap if not already built
  if [ ! -x "tools/sof_wrap/sof_wrap" ]; then
    make -C tools/sof_wrap
  fi

  # Wrap ELF in SOF container (signed if keys available)
  SIGN_ARGS=""
  if [ -f "artifacts/keys/intermediate.seed" ] && [ -f "artifacts/keys/intermediate.cert" ]; then
    SIGN_ARGS="--sign-key artifacts/keys/intermediate.seed --sign-cert artifacts/keys/intermediate.cert"
  fi

  ./tools/sof_wrap/sof_wrap \
    --type bin --name "$APP_NAME" --author "SecureOS" --version "1.0.0" \
    --date "$(date -u +%Y-%m-%d)" \
    $SIGN_ARGS \
    "artifacts/user/$APP_NAME.elf" "artifacts/user/$APP_NAME.bin"
  echo "Built artifacts/user/$APP_NAME.bin"
}

mkdir -p "$OUT_DIR"
build_user_app_inner
echo "PASS: user app build ($APP_NAME)"
