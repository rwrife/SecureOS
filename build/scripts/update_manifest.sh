#!/usr/bin/env bash
# update_manifest.sh - Record current source hashes after a successful build
#
# This script runs INSIDE the Docker toolchain container. It writes the
# build manifest that detect_changes.sh reads on subsequent builds.
#
# Called by: build/scripts/build.sh (after successful build)
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
MANIFEST="$ROOT_DIR/artifacts/.build-manifest"

# Compute hash by listing files via find
hash_files() {
  local search_dirs=("$@")
  local hash_input=""
  local file

  for dir_or_glob in "${search_dirs[@]}"; do
    if [ -d "$dir_or_glob" ]; then
      while IFS= read -r -d '' file; do
        hash_input+="$(md5sum "$file" 2>/dev/null | cut -d' ' -f1)"
      done < <(find "$dir_or_glob" -type f -print0 2>/dev/null | sort -z)
    elif compgen -G "$dir_or_glob" >/dev/null 2>&1; then
      for file in $dir_or_glob; do
        [ -f "$file" ] && hash_input+="$(md5sum "$file" 2>/dev/null | cut -d' ' -f1)"
      done
    fi
  done

  if [ -z "$hash_input" ]; then
    echo "empty"
  else
    echo "$hash_input" | md5sum | cut -d' ' -f1
  fi
}

cd "$ROOT_DIR"

mkdir -p "$(dirname "$MANIFEST")"

cat > "$MANIFEST" <<EOF
# SecureOS Build Manifest - auto-generated by update_manifest.sh
# Do not edit manually. This file is used by detect_changes.sh to
# determine which layers need rebuilding.
timestamp=$(date -u +%Y-%m-%dT%H:%M:%SZ)
keys=$(hash_files "build/scripts/generate_keys.sh" "tools/keygen/")
bearssl=$(hash_files "vendor/bearssl/")
kernel=$(hash_files "kernel/")
grub=$(hash_files "build/grub/")
libs=$(hash_files "user/libs/" "user/include/")
apps=$(hash_files "user/apps/" "user/include/" "user/runtime/")
os_commands=$(hash_files "user/os_commands/")
build_scripts=$(hash_files "build/scripts/build_user_app.sh" "build/scripts/build_user_lib.sh" "build/scripts/build_os_command.sh" "build/scripts/build_kernel_entry.sh" "build/scripts/build_kernel_image.sh")
EOF

echo "[manifest] Updated $MANIFEST"
