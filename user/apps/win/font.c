void font_draw_char(unsigned char *buffer, int buf_stride,
                    int px, int py, char ch, unsigned char color) {
  int row, col;
  unsigned char idx;
  const unsigned char *glyph;

  if (buffer == 0) return;

  idx = (unsigned char)ch;
  if (idx < 0x20 || idx > 0x7E) {
    idx = 0x20; /* render unknown chars as space */
  }
  glyph = font_data[idx - 0x20];

  for (row = 0; row < FONT_CHAR_H; row++) {
    int dy = py + row;
    unsigned char bits;
    if (dy < 0 || dy >= 200) continue;
    bits = glyph[row];
    for (col = 0; col < FONT_CHAR_W; col++) {
      if (bits & (0x10 >> col)) {
        int dx = px + col;
        if (dx < 0 || dx >= buf_stride) continue;
        buffer[dy * buf_stride + dx] = color;
      }
    }
  }
}

void font_draw_string(unsigned char *buffer, int buf_stride,
                      int px, int py, const char *str, unsigned char color) {
  int x = px;
  if (str == 0) return;

  while (*str != '\0') {
    font_draw_char(buffer, buf_stride, x, py, *str, color);
    x += FONT_CHAR_W + 1;
    str++;
  }
}
